        int count = 1;
        do {
            System.out.println("Count is: " + count);
            count++;
        } while (count < 11);
        
    //================================================
//================================================
//================================================
//================================================
//================================================
//================================================
//================================================
        System.out.println("\n\nLine 38: Always goes through at least once.");
        //Our count=10 and the while=10, we still have to run through to see.
        count = 10;
        do {
            System.out.println("Count is: " + count);
            count++;
        } while (count < 10);
//================================================
